				<h4>Why can’t I pay for my items using Humphree?</h4>

				Humphree is a free advertising service and does not provide facilities to enable exchange of monies between two parties.

				All negotiation and exchange of currency is conducted privately between buyer and seller via the Humphree message service. This enables Humphree to provide free advertising space as a service without the overhead of maintaining transactions, and allows Humphree users to maintain more privacy without the need to enter credit card, bank or name and address details.

				<h4>What does ‘ono’ mean on an advertisement?</h4>

