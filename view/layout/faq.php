                <div style="margin-left:40px;">
                    <h4>I have something to sell. How do I place an advertisement on Humphree?</h4>
                    Register as a user and login. Use the Dashboard found under ‘My Account’. Select the ‘Add New Item’
                    option. Choose ‘For Sale’. Select the category that is the closest match for your item and enter the
                    details of the item. Confirm the entered details and your ad is instantly available on Humphree for
                    all to see.

                    <h4>I have something I would like to buy. How do I place an advertisement on Humphree?</h4>
                    Register as a user and login. Use the Dashboard found under ‘My Account’. Select the ‘Add New Item’
                    option. Choose ‘Wanted’. Select the category that is the closest match for your item and enter the
                    details of the item. Confirm the entered details and your ad is instantly available on Humphree for
                    all to see.

                    <h4>I have sold my item. How do I close the advertisement so it doesn't appear as listed
                        anymore?</h4>
                    Login and go to the Dashboard under ‘My Account’. Use the delete option on the action menu adjacent
                    to the advertisement you want to delete.

                    <h4>I don’t want my ad listed anymore. How do I delete it?</h4>
                    Login and go to the Dashboard under ‘My Account’. Use the delete option on the action menu adjacent
                    to the advertisement you want to delete.

                    <h4>I need to change some of the details on my ad. How do I do that?</h4>
                    Login and go to the Dashboard under ‘My Account’. Use the edit option on the action menu adjacent to
                    the advertisement you want to delete.

                    <h4>How do I include an image in my ad?</h4>
                    Use the ‘browse’ button to find the image on your computer and select it for use. You will see a
                    preview of the image. Only jpeg/jpg type images are allowed. The image size is restricted to a
                    maximum of 20MB.

                    <h4>How do I message a Seller or Buyer about their ad?</h4>
                    You need to be a registered user and logged on to Humphree. Once you are logged in, click on the
                    advertisement to see the full details of the ad. At the bottom you will see a box where you can type
                    your message to the advertiser. They will reply to your message when they are next logged in to view
                    their messages.

                    <h4>How do I see my messages?</h4>
                    Under My Account, go to the Dashboard and you will find your messages on the Messages tab.

                    <h4>How do I search for an ad?</h4>
                    You can navigate through the advertising categories to find ads or use the Search function from the
                    Home page or the search bar at the top of every page. Simply type in the keywords you are looking
                    for and click on the magnifying glass to begin the search. The search results will return items
                    where any of the words you entered are found. If you would like to search for a phrase, surround
                    the words in double quotes e.g. “Elvis Costello”. If you would like to <em>exclude</em> a word from
                    the search, prefix the word with a minus sign e.g. -book. Keywords must be more than 3 characters.

                    <h4>How do I search on more than just keywords?</h4>
                    You can search on multiple criteria by using the Advanced Search option located on the main search
                    on the home page. In the advanced search you can search for keywords with a category or
                    sub-category, for items on sale or wanted, with a particular condition, the quantity you need and
                    you can search based on a price range.

                    <h4>How is my ad matched to other ads?</h4>
                    When you enter an advertisement, this triggers a search of other advertisements in the same category
                    where the title and description are similar to your advertisement. The price of the other
                    advertisements must be within a 50% variance (up or down) of the price on your own advertisement.
                    Once matches are found, a message is added to your action list so you may view the matched
                    advertisements.

                    <h4>What are Action Items?</h4>
                    Under Action Items you will find a list of advertisements that match your own advertisement. From
                    the Action Items menu, you can view the details of the match, send a message to the advertiser,
                    accept the match, or discard it if it is not what you were looking for.

                    <h4>What happens once I accept a match?</h4>
                    You will see a thumbs up at your side, and the advertiser you are accepting will also see a thumbs
                    up. If they agree to trade with you the will also accept their match and you will see 2 thumbs up in
                    your action list.

                    <h4>Why did I receive an email to give Feedback on my transaction?</h4>
                    If you were provided with a good service and honesty, then it is nice to give the advertiser some
                    positive feedback. The rating will show against their advertisements and help others to know they
                    are a trustworthy source.

                    <h4>What does “Not yet rated” mean?</h4>
                    It means that no-one has provided a rating on this seller yet. Either they are new to Humphree or
                    correspondents have not been kind enough to submit feedback.
