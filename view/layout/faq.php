                    Negotiate with the seller privately and make arrangements to collect the item for sale.

                    <h4>I have something that is listed as wanted. How do I contact the buyer?</h4>
                    To contact a person who has posted a wanted advertisement, you must register as a user and contact
                    the buyer directly via the Humphree private messaging service.
                    Negotiate with the buyer privately and make arrangements to have them collect the item.

                    <h4>Why can’t I pay for my items using Humphree?</h4>
                    Humphree is a free advertising service and does not provide facilities to enable exchange of monies
                    between two parties.
                    All negotiation and exchange of currency is conducted privately between buyer and seller via the
                    Humphree message service. This enables Humphree to provide free advertising space as a service
                    without the overhead of maintaining transactions, and allows Humphree users to maintain more privacy
                    without the need to enter credit card, bank or name and address details.

                    <h4>What does ‘ono’ mean on an advertisement?</h4>
                    ‘Or Near Offer’ - this means that the trader is willing to negotiate and is not necessarily
                    expecting the full advertised price to be paid, but something close to it.

