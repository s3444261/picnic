                    with them.

                    <h4>How do I register?</h4>
                    On the login page, click the ‘Sign up Now!’ button. You will be asked for your email address,
                    username, and password. On submitting these details you will be sent an activation link via email.
                    Click on the activation link in the email to activate your account.

                    <h4>Why do I need to register?</h4>
                    You won’t be able to contact advertisers if you do not register. Registration is to safeguard
                    advertisers from unwanted and unsolicited calls. Don’t worry, we do not sell your information to 3rd
                    parties. Please read our <a href=<?php echo BASE . '/Help/TermsOfService' ?>>Terms and
                        Conditions</a> and <a href=<?php echo BASE . '/Help/PrivacyPolicy' ?>>Privacy Policy</a> before
                    registering.

                    <h4>I’ve forgotten my username or password. How can I recover it?</h4>
                    If you've forgotten your username, you can log in with your email address.
                    If you've forgotten your password, select ‘Forgot password?’ and follow the prompts. Once done,
                    you'll receive an email with a new temporary password. You should sign in with that password and
                    immediately change it.
                    If you're having trouble signing in, try the following solutions:
                    <ul>
