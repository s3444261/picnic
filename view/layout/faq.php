                    </ul>


                    <h4>How do I change my password?</h4>
                    Go to the ‘My Account’ menu and choose the option Change Password.
                    Enter your current password and the new password you want to use. Click the Change Password button
                    to activate the password change.

                    <h4>How do I log out?</h4>
                    Use the ‘logout’ option under the ‘My Account’ menu options.

                    <h4>Why do I have to log in again sometimes?</h4>
                    If you have left yourself logged in to Humphree you will automatically be logged out after a period
                    of inactivity. This is a security measure to keep your details safe.
